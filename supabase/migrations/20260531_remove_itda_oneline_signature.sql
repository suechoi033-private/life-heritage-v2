-- =============================================================
-- contents body 끝의 "— 잇다 한 줄" 시그니처 라인 일괄 삭제
--
-- 사장님 결정: "잇다 한 줄" 표시는 모든 콘텐츠에서 제거.
--   - 카드/하이라이트 형식이 무거움
--   - 큐레이션 자체가 잇다 에디터 글이므로 시그니처 중복
--
-- 패턴: 마지막에 "잇다 한 줄"이 들어간 한 줄 전체 제거 (앞 공백·빈 줄 포함).
-- 안전:
--   * `category='reflection'`만 대상 (다른 카테고리 영향 없음)
--   * 멱등 — 이미 제거된 행은 변화 없음
--   * 트랜잭션 — 실패 시 롤백
-- 적용: Supabase SQL Editor에서 1회 실행.
-- =============================================================

begin;

update public.contents
set body = regexp_replace(
  body,
  E'\\s*\\n+[^\\n]*잇다\\s*한\\s*줄[^\\n]*\\s*$',
  '',
  'g'
)
where category = 'reflection'
  and body ~ '잇다 한 줄';

commit;
