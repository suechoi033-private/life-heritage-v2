## 미리 정리하는 건, 나를 위한 일이기도 합니다

디지털 정리는 죽음을 준비하는 일처럼 보이지만, 사실 해 보면 **지금 내 삶을 한 번 정돈하는 일**에 더 가깝습니다. 안 쓰는 구독을 끊고, 흩어진 계정을 추리고, 중요한 사진을 한곳에 모으는 동안 — 정작 정리되는 건 내 현재입니다.

특히 곁에 챙겨 줄 사람이 가까이 없는 1인 가구나 비혼이라면, 이 준비는 더 중요합니다. 아무도 내 디지털을 자동으로 정리해 주지 않기 때문입니다. 누구에게 무엇을 맡길지 미리 정해 두는 것 자체가, 나를 지키는 일입니다.

그리고 이 글을 읽고 한 가지라도 정리했다면 — 같은 고민을 하는 다음 사람에게 당신의 방법을 남겨 주세요. 그게 잇다가 만들어지는 방식입니다.

