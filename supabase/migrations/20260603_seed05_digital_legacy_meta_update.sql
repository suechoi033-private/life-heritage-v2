## 7. 가장 큰 벽 — 비밀번호와 2단계 인증

앞의 모든 것을 가로막는 단 하나가 바로 이것입니다. 비밀번호를 모르고, 2단계 인증 문자가 고인의 휴대폰으로만 가고, 그 휴대폰조차 잠겨 있다면 — 가족은 어느 문도 열지 못합니다.

그래서 권하는 방식은 두 가지입니다.

1. **플랫폼이 제공하는 공식 장치를 미리 켜 둔다** (구글 휴면 계정 관리자, 애플 디지털 유산처럼).
2. **계정 목록과 접근 방법을 정리해, 신뢰하는 사람에게 위치를 알려 둔다.** 비밀번호 자체를 평소에 노출하기보다, "내게 무슨 일이 생기면 여기를 보라"는 안내를 남겨 두는 것입니다.

완벽하게 다 할 필요는 없습니다. 가장 중요한 것부터 — 메일·사진이 몰린 계정, 자동결제가 걸린 카드, 금융자산 목록 — 하나씩 적어 두는 것만으로도 남은 가족의 막막함은 크게 줄어듭니다.

