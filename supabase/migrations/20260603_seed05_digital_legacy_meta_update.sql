## 6. 금융·간편결제·구독 — '목록'을 남기는 것이 핵심

은행·증권 같은 금융자산은 상속 절차에 따라 처리되지만, 그 전에 가족이 부딪히는 더 현실적인 문제는 "**무엇이, 어디에 있는지를 모른다**"는 것입니다.

- **금융계좌·증권:** 어느 은행·증권사에 무엇이 있는지 목록. (사망 후에는 '안심상속 원스톱 서비스'로 통합 조회도 가능합니다.)
- **간편결제·페이:** 충전금·포인트가 남아 있을 수 있습니다.
- **정기 구독:** OTT·음악·클라우드·멤버십 등 매달 자동결제되는 항목. 정리하지 않으면 떠난 뒤에도 계속 빠져나갑니다.

이 모두를 한 번에 해결하는 비밀번호 공유는 위험하고 권하지 않습니다. 대신 **"어디에 무엇이 있다"는 목록**을 만들어, 신뢰하는 가족이 필요할 때 찾아갈 수 있도록 남겨 두는 것이 안전한 방법입니다.

