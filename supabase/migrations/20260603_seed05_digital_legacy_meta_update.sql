# 디지털 계정·자산 정리 — 내가 떠난 뒤 가족이 헤매지 않게

요즘 한 사람이 떠나면, 남는 건 유품만이 아닙니다.

이메일, 사진 수만 장, SNS 계정, 은행·증권 앱, 간편결제, 매달 빠져나가는 구독료, 그리고 어딘가에 잠겨 있는 비밀번호들. 이른바 '디지털 유산'입니다. 문제는 이 대부분이 **본인만 열 수 있도록** 만들어져 있다는 점입니다. 비밀번호와 2단계 인증, 잠금화면 — 살아 있을 땐 나를 지켜 주던 장치가, 떠난 뒤엔 가족 앞을 막는 벽이 됩니다.

실제로 가족이 가장 많이 막히는 지점이 여기입니다. 고인의 휴대폰 잠금을 못 풀어 인증 문자를 못 받고, 그래서 은행 앱도 못 열고, 매달 나가는 구독료가 무엇인지조차 파악하지 못합니다. 그래서 디지털 정리는 "내가 떠난 뒤를 위한 배려"이면서, 동시에 "지금 내가 할 수 있는 가장 현실적인 준비"입니다.

플랫폼마다 미리 해 둘 수 있는 장치가 다릅니다. 하나씩 짚어 드리겠습니다.

---

## 1. 구글 — '휴면 계정 관리자'를 미리 켜 두기

구글에는 [**휴면 계정 관리자(Inactive Account Manager)**](https://support.google.com/accounts/answer/3036546?hl=ko)라는 기능이 있습니다. 내 계정이 일정 기간(3개월~18개월 사이에서 내가 정함) 사용되지 않으면, 미리 지정해 둔 사람에게 내 데이터(사진·이메일 등 내가 고른 항목)를 전달하거나 계정을 삭제하도록 설정하는 기능입니다. **최대 10명까지** 신뢰할 사람을 지정할 수 있고, 각 사람에게 어떤 항목을 줄지도 따로 고를 수 있습니다.

이메일과 사진이 구글에 몰려 있는 분이 많은 만큼, **살아 있을 때 한 번 켜 두면** 가장 큰 벽 하나를 미리 치워 두는 셈입니다.

→ 근거: [Google 계정 고객센터 '휴면 계정 관리자'](https://support.google.com/accounts/answer/3036546?hl=ko)

