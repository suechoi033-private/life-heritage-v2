## 3. 페이스북 — 사전에 '기념 계정 관리자'를 정해 두기

페이스북(Meta)에는 두 갈래의 길이 있습니다. 하나는 **생전에 본인이 미리 정해 두는 길**, 다른 하나는 **떠난 뒤 가족이 요청하는 길**입니다.

**(1) 생전 — 기념 계정 관리자(Legacy Contact) 지정**
페이스북 설정에서 신뢰하는 친구·가족 한 명을 [**기념 계정 관리자**](https://www.facebook.com/help/1568013990080948/?locale=ko_KR)로 미리 정해 둘 수 있습니다. 내가 떠난 뒤 계정이 '기념 계정'으로 전환되면, 이 사람이 프로필 사진 변경·고정 게시물 작성·새 친구 요청 수락 같은 제한된 관리를 할 수 있습니다. **단, 비밀번호로 로그인하거나 메시지를 읽을 수는 없습니다.** 설정·변경·삭제 방법은 [여기](https://www.facebook.com/help/1070665206293088/?locale=ko_KR)에서 확인.

**(2) 사후 — 가족·지인의 요청**
미리 지정해 두지 않았어도, 가족·지인이 [**기념 계정 전환 또는 계정 삭제 요청**](https://www.facebook.com/help/1111566045566400/?locale=ko_KR) 페이지에서 신청할 수 있습니다. **기념 계정 전환**(프로필에 'Remembering' 표시가 붙고 보존)과 **영구 삭제** 두 옵션이 있고, **영구 삭제는 직계가족(또는 유언집행인)임을 증빙**해야 합니다. 보통 사망진단서·부고 링크 등 사망 사실 증빙과 관계 증빙을 함께 제출합니다.

*팁: 페이스북에 사진·추억이 많이 남아 있다면, '삭제'보다 '기념 계정 전환' 쪽을 한 번 더 생각해 보세요. 한번 삭제하면 되돌릴 수 없습니다.*

→ 근거: [Facebook 고객센터 '기념 계정 관리자 정보'](https://www.facebook.com/help/1568013990080948/?locale=ko_KR) · [기념 계정 전환·삭제 요청](https://www.facebook.com/help/1111566045566400/?locale=ko_KR)

