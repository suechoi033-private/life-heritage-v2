## 4. 인스타그램 — 사후 '기념 계정' 또는 '삭제' 요청

인스타그램은 페이스북과 달리 **생전에 본인이 관리자를 미리 지정해 두는 기능이 없습니다.** (페이스북의 Legacy Contact 같은 사전 지정 기능 없음.) 그래서 인스타그램은 전적으로 **떠난 뒤 가족이 요청하는 방식**입니다.

두 가지 옵션이 있습니다.

- **기념 계정으로 전환**: 누구든 사망 사실을 알리는 [**사망자 프로필 신고**](https://help.instagram.com/264154560391256/) 양식으로 요청할 수 있습니다. 부고 기사 링크 같은 사망 증빙이 필요합니다. 전환되면 이름 옆에 'Remembering'이 붙고, 게시물은 그대로 남되 누구도 로그인하거나 변경할 수 없습니다.
- **계정 영구 삭제**: **직계가족만** 요청할 수 있고, 직계가족임을 증빙(가족관계 서류 등)하고 사망 증빙을 함께 제출해야 합니다. 법적 대리인의 경우에는 권한을 증빙해야 합니다. ([Instagram 기념 계정 정보(한국어)](https://www.facebook.com/help/instagram/231764660354188/?locale=ko_KR))

*아쉽지만 인스타그램은 '미리 해 둘 수 있는 일'이 거의 없습니다. 그래서 더더욱 — 내가 어떤 SNS를 쓰고 있는지, 무엇을 남기고 싶고 무엇을 지우고 싶은지를 **글로 남겨 두는 일**이 중요합니다.*

→ 근거: [Instagram 고객센터 '기념 계정 정보'](https://www.facebook.com/help/instagram/231764660354188/?locale=ko_KR) · [사망자 프로필 신고](https://help.instagram.com/264154560391256/)

