## 2. 애플 — '디지털 유산(Legacy Contact)' 등록

아이폰·아이클라우드를 쓴다면 애플의 [**디지털 유산 연락처(Legacy Contact)**](https://support.apple.com/ko-kr/102631)를 등록해 둘 수 있습니다. 미리 지정해 둔 사람이, 내가 떠난 뒤 **사망 증명서와 함께 애플에 요청**하면 내 iCloud의 사진·메모·연락처 등에 접근할 수 있게 하는 기능입니다. **최대 5명까지** 지정할 수 있습니다(iOS 15.2·iPadOS 15.2·macOS Monterey 12.1 이상에서 지원).

핵심은 **'생전에 미리' 등록해 둬야 한다**는 점입니다. 등록해 두지 않으면, 남은 가족이 애플 데이터에 접근하기는 훨씬 어려워집니다.

→ 근거: [Apple 지원 '디지털 유산 연락처 추가하기'](https://support.apple.com/ko-kr/102631)

