## 그 밖에 자주 보이는 말

### 피상속인 / 상속인
**피상속인**은 사망하여 재산을 남긴 사람(고인), **상속인**은 그 재산을 물려받는 사람을 말합니다. 헷갈리기 쉬운 짝이라 함께 기억해 두면 좋습니다.
→ 출처: [찾기쉬운 생활법령정보 '상속'](https://easylaw.go.kr/CSP/CnpClsMain.laf) *(출처 확인 필요)*

### 상속개시
법적으로 상속이 시작되는 시점을 말하며, 보통 피상속인의 사망 시점입니다. 상속포기·한정승인 등 여러 기한이 '상속개시를 안 날'을 기준으로 계산되므로 중요한 개념입니다.
→ 출처: [찾기쉬운 생활법령정보 '상속의 개시'](https://easylaw.go.kr/CSP/CnpClsMain.laf) *(출처 확인 필요)*

### 유족연금
국민연금 등에 가입한 사람이 사망했을 때, 유족이 받을 수 있는 연금입니다. 청구에는 소멸시효가 있으며, 공무원·사학·군인연금은 각 공단으로 따로 문의해야 합니다.
→ 출처: [국민연금공단 '유족연금'](https://www.nps.or.kr/pnsinfo/ntpsklg/getOHAF0072M0.do)
→ 관련 가이드: [사별 후 90일 체크리스트](./content-detail.html?id=313c6616-0bae-4d10-bc0a-74573694f487)

### 디지털 유산
고인이 남긴 디지털 흔적 — 휴대폰·온라인 계정·사진·구독 서비스 등을 말합니다. 비밀번호나 본인인증 문제로 유족이 접근하지 못하는 경우가 많아, 미리 정리해 두는 것이 점점 중요해지고 있습니다.
→ 관련 글: [유서가 아니라, 비밀번호 목록을 먼저 썼습니다](./content-detail.html?id=7591c5d9-a011-4c9d-b162-e9760baf7b5d)

