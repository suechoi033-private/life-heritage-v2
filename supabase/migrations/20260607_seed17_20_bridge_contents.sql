## 왜 '재산보다 빚을 먼저' 봐야 하나

이 서비스를 가장 먼저 신청하라고 권하는 데는 이유가 있습니다.

상속은 재산만 물려받는 게 아니라 **빚도 함께 물려받는** 제도입니다. 만약 고인의 빚이 재산보다 많은데 그걸 모른 채 상속을 받아들이면, 자식이 그 빚을 떠안게 될 수 있습니다.

그래서 순서가 중요합니다. **먼저 안심상속으로 재산과 빚의 전체 그림을 본 뒤**, 빚이 더 많다면 '상속포기'나 '한정승인' 같은 제도를 검토하게 됩니다. 이 결정에는 짧은 법정 기한이 있어, 재산 파악이 늦어지면 판단할 시간도 함께 줄어듭니다.

다만 상속포기·한정승인은 법적 효력과 절차가 까다로워, 판단은 상황에 따라 크게 달라집니다. **정확한 결정은 반드시 법무사·변호사 등 전문가와 상담하시기 바랍니다.** (관련 절차의 기한별 정리는 잇다 '사별 후 90일 체크리스트'에서 함께 보실 수 있습니다.)

