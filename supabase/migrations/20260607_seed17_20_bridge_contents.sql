## 받을지 말지 — 승인과 포기

### 상속포기
상속을 아예 받지 않겠다고 가정법원에 신고하는 것입니다. 재산도 빚도 모두 받지 않게 됩니다. 빚이 재산보다 많을 때 검토하며, **법정 기한이 짧다**는 점이 중요합니다. 기한과 효력은 상황에 따라 다르므로 전문가 확인이 필요합니다.
→ 출처: [민법 제1019조](https://casenote.kr/%EB%B2%95%EB%A0%B9/%EB%AF%BC%EB%B2%95/%EC%A0%9C1019%EC%A1%B0), [찾기쉬운 생활법령정보 '상속의 승인·포기'](https://easylaw.go.kr/CSP/CnpClsMain.laf?popMenu=ov&csmSeq=255&ccfNo=4&cciNo=3&cnpClsNo=1)
→ 관련 가이드: [사별 후 90일 체크리스트](./content-detail.html?id=313c6616-0bae-4d10-bc0a-74573694f487)

### 한정승인
물려받은 재산의 한도 안에서만 고인의 빚을 갚겠다고 신고하는 것입니다. 빚이 재산보다 많은지 애매할 때 검토하는 제도이며, 절차가 까다로워 흔히 전문가 상담을 권합니다. 역시 **법정 기한**이 있습니다.
→ 출처: [민법 제1019조](https://casenote.kr/%EB%B2%95%EB%A0%B9/%EB%AF%BC%EB%B2%95/%EC%A0%9C1019%EC%A1%B0), [찾기쉬운 생활법령정보 '상속의 승인·포기'](https://easylaw.go.kr/CSP/CnpClsMain.laf?popMenu=ov&csmSeq=255&ccfNo=4&cciNo=3&cnpClsNo=1)
→ 관련 가이드: [사별 후 90일 체크리스트](./content-detail.html?id=313c6616-0bae-4d10-bc0a-74573694f487)

### 단순승인
재산과 빚을 모두 그대로 물려받는 것입니다. 별도의 신고 없이 일정 행위를 하거나 기한이 지나면 단순승인한 것으로 보는 경우가 있어, 포기·한정승인을 생각한다면 그 전에 주의가 필요합니다.
→ 출처: [찾기쉬운 생활법령정보 '상속의 승인·포기'](https://easylaw.go.kr/CSP/CnpClsMain.laf?popMenu=ov&csmSeq=255&ccfNo=4&cciNo=3&cnpClsNo=1)

