## 누가, 어디서, 언제까지 신청하나

**신청은 어디서** — 가까운 시·군·구청이나 행정복지센터를 방문하거나, [정부24](https://www.gov.kr/portal/onestopSvc/safeInheritance)에서 온라인으로 신청할 수 있습니다. 사망신고를 하면서 함께 신청하는 경우가 많습니다.

**신청 자격** — 상속인(또는 그 대리인) 등 일정 자격을 갖춘 사람이 신청할 수 있습니다. 본인 확인 서류와 가족관계를 증명하는 서류가 필요하며, 구체적인 요건은 상황에 따라 다르므로 신청 전 정부24나 행정복지센터에서 확인하는 것이 안전합니다.

**신청 기한** — 신청에는 기한이 정해져 있습니다. 기한을 넘기면 이 통합조회 제도를 이용하기 어려워질 수 있으므로, 사망신고 시점에 함께 알아두는 것이 좋습니다. 정확한 기한은 매 시점 정부24 공지를 확인하세요.

