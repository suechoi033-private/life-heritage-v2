## 안심상속 원스톱 서비스란

정식 명칭은 **'사망자 등 재산조회 통합처리 신청(안심상속 원스톱 서비스)'**입니다. 행정안전부가 운영하며, **한 번의 신청으로** 고인의 여러 재산·채무 정보를 한꺼번에 조회할 수 있게 해주는 제도입니다.

조회할 수 있는 항목은 대체로 다음과 같습니다.

- **금융** — 예금, 대출, 보험 가입 내역 등 금융거래 정보
- **국세·지방세** — 체납액, 미납 세금
- **연금** — 국민연금·공무원연금 등 가입 여부
- **부동산·자동차** — 토지, 건축물, 자동차 소유 내역

흩어진 정보를 각 기관에 따로 묻지 않고 한 번에 받아볼 수 있다는 점이 핵심입니다.

