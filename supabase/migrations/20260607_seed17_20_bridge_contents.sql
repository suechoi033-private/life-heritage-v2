## 나누고, 지키고 — 분할과 유류분

### 상속재산분할
공동상속인이 물려받은 재산을 누가 무엇을 가질지 나누는 것입니다. 협의로 정하는 게 원칙이고, 협의가 안 되면 가정법원의 조정·심판을 거칩니다. 분쟁이 가장 자주 생기는 단계입니다.
→ 출처: [찾기쉬운 생활법령정보 '상속재산의 분할'](https://easylaw.go.kr/CSP/CnpClsMain.laf) *(출처 확인 필요)*

### 유류분
유언이나 증여로 특정인에게 재산이 쏠려도, 일정 범위의 상속인(자녀·배우자 등)이 법으로 최소한 보장받는 몫을 말합니다. 자신의 유류분이 침해됐다면 반환을 청구할 수 있으며, 여기에도 청구 기한이 있습니다.
→ 출처: [찾기쉬운 생활법령정보 '유류분'](https://easylaw.go.kr/CSP/CnpClsMain.laf) *(출처 확인 필요)*

### 기여분
고인을 특별히 부양했거나 재산을 늘리는 데 특별히 기여한 상속인이, 그 기여를 인정받아 상속분에 더해 받을 수 있는 몫입니다. 인정 여부와 범위는 협의 또는 법원의 판단으로 정해집니다.
→ 출처: [찾기쉬운 생활법령정보 '기여분'](https://easylaw.go.kr/CSP/CnpClsMain.laf) *(출처 확인 필요)*

### 유언(유언장)
재산을 누구에게 어떻게 남길지 등을 생전에 정해두는 법적 의사표시입니다. 법이 정한 방식(자필증서·공정증서 등)을 따르지 않으면 효력이 인정되지 않을 수 있어, 형식 요건이 중요합니다.
→ 출처: [찾기쉬운 생활법령정보 '유언'](https://easylaw.go.kr/CSP/CnpClsMain.laf) *(출처 확인 필요)*

