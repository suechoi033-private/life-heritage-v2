## 용어를 다 알아도 남는 한 가지

이 용어들을 다 익혀도, 막상 닥치면 가장 어려운 건 따로 있습니다. **고인이 어디에 무엇을, 어떤 마음으로 남겼는지 아무도 모른다는 것.** 통장의 위치도, 보험의 존재도, "이건 이렇게 해다오"라는 뜻도 — 용어집에는 없습니다.

그래서 가장 확실한 대비는, 떠나는 사람이 미리 한곳에 적어두는 일입니다. 남는 사람이 사전을 뒤지지 않아도 되도록.

> **(지금 정리가 필요하다면)** [**사별 후 90일 체크리스트**](./content-detail.html?id=313c6616-0bae-4d10-bc0a-74573694f487) — 무엇을·언제까지·어디서 해야 하는지 순서대로.
>
> **(내 차례를 준비하고 싶다면)** [**잇다 '디지털 보관함'**](./self.html) — 어디에 무엇이 있는지, 어떤 마음이었는지 한곳에 모아 둘 수 있습니다.

