## 이 제도가 있어도, 끝내 못 찾는 것이 있다

안심상속 원스톱 서비스는 분명 큰 도움이 됩니다. 흩어진 통장과 빚의 윤곽을 한 번에 그려주니까요.

그런데 이 제도로도 알 수 없는 것이 있습니다.

고인의 휴대폰 잠금번호. 그 안의 사진과 메시지. 매달 빠져나가던 구독료가 무엇이었는지. 그리고 무엇보다 — **"이 돈은 이런 마음이었다", "형제끼리 이건 이렇게 해다오"라는 고인의 뜻.** 제도는 재산의 '목록'은 찾아주지만, 그 재산에 담긴 '마음'까지 찾아주지는 못합니다.

상속을 둘러싼 다툼의 많은 부분이 사실 돈의 액수가 아니라, **'고인의 뜻을 아무도 모른다'**는 데서 시작된다고들 합니다. 남겨진 게 재산이 아니라 물음표일 때, 가족은 각자 짐작한 고인을 두고 부딪힙니다.

