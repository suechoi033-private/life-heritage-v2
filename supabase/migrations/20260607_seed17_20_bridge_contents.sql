## 세금 — 숫자는 반드시 공식 출처에서

> 세율·공제 한도·면제 기준 같은 **숫자는 해마다 바뀝니다.** 아래 정의만 참고하시고, 정확한 금액은 반드시 [국세청 홈택스](https://www.hometax.go.kr) 또는 [국세청 누리집](https://www.nts.go.kr)에서 최신 기준을 확인하세요.

### 상속세
사람이 사망하여 남긴 재산을 물려받을 때 부과되는 세금입니다. 배우자·자녀 공제 등으로 실제 낼 세금이 없는 경우도 있지만, 신고 기한이 있고 신고 자체가 권장되는 경우가 많습니다. 공제 한도·세율은 매년 확인이 필요합니다.
→ 출처: [국세청 '상속세 신고시 유의사항'](https://www.nts.go.kr/nts/cm/cntnts/cntntsView.do?mi=2328&cntntsId=7722), 금액은 [국세청 홈택스](https://www.hometax.go.kr)에서 확인

### 증여세
살아 있는 사람이 다른 사람에게 재산을 무상으로 줄 때, 받은 사람에게 부과되는 세금입니다. 가족 간 증여에는 일정 공제가 있으나 한도·세율은 자주 바뀌므로 본문에 적지 않습니다. 정확한 금액은 공식 출처에서 확인하세요.
→ 출처: [국세청 '증여세' 안내](https://www.nts.go.kr) *(국세청 누리집 증여세 안내 페이지 — deep-link 확인 필요)*, 금액은 [국세청 홈택스](https://www.hometax.go.kr)에서 확인

### 상속공제 / 면제 한도
상속세를 계산할 때 일정 금액을 재산에서 빼주는 제도입니다. 기초공제, 배우자공제, 일괄공제 등 종류가 여러 가지이며, 한도와 적용 요건은 상황과 연도에 따라 다릅니다. 정확한 금액은 반드시 공식 출처에서 확인하세요.
→ 출처: [국세청 '상속세 신고시 유의사항'](https://www.nts.go.kr/nts/cm/cntnts/cntntsView.do?mi=2328&cntntsId=7722), 금액은 [국세청 홈택스](https://www.hometax.go.kr)에서 확인

### 취득세(상속분)
상속으로 부동산·자동차 등을 취득할 때 내는 지방세입니다. 신고·납부 기한이 있어, 등기를 미루더라도 세금 기한은 따로 챙겨야 합니다. 세율·기한은 공식 출처에서 확인하세요.
→ 출처: [위택스(지방세 신고·납부)](https://www.wetax.go.kr) *(출처 확인 필요)*, [찾기쉬운 생활법령정보 '상속에 의한 소유권 이전등기'](https://easylaw.go.kr/CSP/CnpClsMain.laf?popMenu=ov&csmSeq=566&ccfNo=3&cciNo=3&cnpClsNo=1)

