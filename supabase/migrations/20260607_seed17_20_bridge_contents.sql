## 부모님 살아계실 때 알아두면 달라지는 것

여기까지 읽으신 분이라면, 한 가지 생각이 스쳤을지도 모릅니다.

언젠가 내 아이도 똑같이 할 거라는 생각. 내 통장을 찾아 헤매고, 내 휴대폰 앞에서 멈춰 서고, "엄마라면 어떻게 했을까"를 두고 다투는 일. **'나는 내 아이에게는 이걸 겪게 하고 싶지 않다.'** 강요가 아닙니다. 한 번이라도 이 절차를 가까이서 본 사람이라면 자연스럽게 떠오르는 생각입니다. 그 생각이 드는 순간이, 사실은 시작입니다.

안심상속 원스톱 서비스가 자식이 *떠난 뒤에* 흩어진 것을 모아주는 제도라면, 그 수고를 *애초에 없애는* 길은 하나입니다. 부모가 살아계실 때, 어디에 무엇이 있는지 한곳에 모아두는 것. 거창한 유언이 아니라, 통장이 어디 있는지 한 줄, 무엇을 열려면 무엇이 필요한지 한 줄, 그리고 남기고 싶은 마음 한 줄.

그 몇 줄이 있으면, 자식은 두 달을 헤매지 않아도 됩니다.

