### 대습상속
원래 상속받을 사람(예: 자녀)이 피상속인보다 먼저 사망했거나 상속 자격을 잃은 경우, 그 사람의 직계비속(예: 손자녀)이나 배우자가 대신 상속받는 것을 말합니다.
→ 출처: [민법 제1001조(대습상속)](https://www.law.go.kr/법령/민법) *(출처 확인 필요 — 법제처 국가법령정보센터에서 조문 확인)*

### 상속분
공동상속인이 각각 얼마만큼의 비율로 상속받는지를 말합니다. 유언이 없으면 법이 정한 비율(법정상속분)을 따르며, 배우자는 다른 상속인보다 일정 비율 더 받습니다. 정확한 계산은 상속인 구성에 따라 달라집니다.
→ 출처: [찾기쉬운 생활법령정보 '법정상속분'](https://easylaw.go.kr/CSP/CnpClsMain.laf) *(출처 확인 필요)*

