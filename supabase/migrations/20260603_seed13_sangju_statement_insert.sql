# 상주진술서, 가장 막막한 순간에 가장 정확한 기록이 자산이 됩니다

부모님이 돌아가신 그날, 가족이 가장 먼저 마주하는 서류는 사망진단서가 아니라 **자기 자신의 진술**인 경우가 많습니다.

병원에서 오래 입원해 계셨고 담당 의사가 임종을 지켜봤다면 그분이 사망진단서를 그대로 적어 주십니다. 하지만 자택에서, 요양원에서, 또는 외출 중에 갑자기 돌아가셨다면 — 이야기가 달라집니다. 시신을 처음 본 의사(검안의)는 고인의 평소 병이나 약, 최근의 변화, 마지막 며칠을 모릅니다. 그래서 가족에게 묻습니다. "언제부터 안 좋으셨어요? 어떤 약을 드시고 계셨어요? 그날은 어땠어요?"

이때 가족이 진술한 내용이 적힌 문서가, 흔히 **상주진술서**라고 불립니다. 정해진 단일 양식이 있는 공식 서류라기보다는, 시체검안서·변사 조사기록·보험금 청구서의 '사망 경위' 칸에 들어가는 **가족의 진술**을 묶어 부르는 표현에 가깝습니다. 의사·경찰·보험사가 각자 자기 양식에 받아 적되, 내용은 결국 같은 것 — **고인이 어떤 사람이었고, 어떻게 떠나셨는가**입니다.

이 글은 "상주진술서가 뭔지" 정의로 시작하지 않습니다. **누가 언제 왜 가족에게 진술을 받는지**, 그 자리에서 무엇을 물어보는지, 무엇을 미리 정리해 두면 덜 흔들리는지를 정리합니다.

---

## 누가, 언제 진술을 받나 — 세 갈래

### 1) 검안의 (자택·요양원·외부 사망의 경우)

병원이 아닌 곳에서 돌아가시면, 119가 와도 사망을 그 자리에서 확정하지 않습니다. 112에 신고해 경찰이 출동하고, 경찰이 부른 **검안의(시신을 검안하는 의사)** 가 와서 시신을 살펴본 뒤 **시체검안서**를 작성합니다. 시체검안서는 사망진단서와 같은 법적 효력을 갖는 서류입니다. (사망진단서·시체검안서는 [의료법 시행규칙 별지 제6호 서식](https://www.law.go.kr/lsLawLinkInfo.do?lsJoLnkSeq=900518283) 하나로 통합되어 있습니다.)

검안의는 고인을 진료해 온 사람이 아니기 때문에, 사망 원인을 판단하기 위해 **가족의 진술에 상당 부분 의지**합니다. 한 의학계 자료는 "시체검안서의 대상이 되는 시신들은 병력과 같은 의학적인 정보나 사망 당시의 상황을 파악하기 어려울 때가 많다"고 명시합니다(출처: [예지부고 — 사망진단서/시체검안서 가이드](https://www.yejibugo.co.kr/blog/%EC%82%AC%EB%A7%9D%EC%A7%84%EB%8B%A8%EC%84%9C%EC%8B%9C%EC%B2%B4%EA%B2%80%EC%95%88%EC%84%9C-%EB%B0%9C%EA%B8%89-%EB%B0%A9%EB%B2%95%EA%B3%BC-%EC%9A%A9%EB%8F%84-%EC%B4%9D%EC%A0%95%EB%A6%AC/) 인용). 그래서 검안의는 사망 시각, 직전 며칠의 증상, 평소 병력, 복용 중인 약을 묻습니다. 의료법시행규칙상의 작성 지침도 "가족이나 목격자의 진술에 의한 경우에는 사후변화와 모순되는 점이 없는가를 확인하고, 진술대로 적되 **누구의 진술에 의한 것인지를 같이 적는다**"고 안내합니다([출처 확인 필요 — 정확한 법령 위치는 감수 시 확정]).

### 2) 경찰 (변사 조사)

자택·요양원·외부에서의 사망은 원칙적으로 **변사(變死)** 로 다뤄집니다. 변사는 "범죄가 의심되는 죽음"만이 아니라, **병사 이외의 죽음 또는 병사가 아닐 가능성이 있는 죽음** 전반을 가리킵니다. 자살·교통사고·자연재해·예상 못한 자택 급사 모두 포함됩니다(출처: [의협신문 — 대한법의학회 변사 가이드라인](https://www.doctorsnews.co.kr/news/articleView.html?idxno=126609)).

경찰은 현장 확인과 함께 가족에게 **간단한 조사(진술)**를 진행합니다. 타살 정황이 없다고 판단되면 검안의가 시체검안서를 발급하고 시신은 장례식장으로 옮겨집니다. 이때 경찰이 작성하는 변사 조사기록이 추후 **변사 사실확인원**(정식 명칭: [(사건사고·화재·도난·변사) 사실확인원](https://www.gov.kr/mw/AA020InfoCappView.do?HighCtgCD=A08003&CappBizCD=13200000063))의 근거가 됩니다. 이 서류는 정부24가 아닌 **관할 경찰서·지구대 방문**으로만 발급되며, 보험금 청구 시(특히 자살·사고사·외인사) 보험사가 요구하는 핵심 서류입니다.

### 3) 보험사 (사망보험금 청구)

장례가 끝나면 보험사가 사망보험금 청구서를 보내옵니다. 그 안에 "**사고(질병) 발생일자**", "**사망원인(진단명)**", **사망 경위** 칸이 있습니다. 손해보험은 사망을 **질병사망**과 **상해사망(재해사망)** 으로 구분해 보장 범위와 보험금이 크게 달라지기 때문에, 이 칸에 적히는 한 문단이 결국 돈의 흐름을 좌우합니다(출처: [한화생명 — 사망보험금 구비서류 안내](https://www.hanwhalife.com/static/main/myPage/insurance/accident/document/MY_INAPADC_T20000.jsp), [DB생명 — 사망 시 청구서류 세부 설명](https://www.idblife.com/support/guide/acbf_clm/detail2)).

기왕증(평소 앓던 병)이 사망에 어떻게 영향을 미쳤는가가 쟁점이 되는 경우가 많고, 보험사는 **6개월~1년 범위의 의무기록과 일자별 진료 경과**를 함께 요구하기도 합니다. 가족이 평소에 정리해 둔 기록이 없으면, 이 시기에 병원을 다시 돌며 자료를 모아야 합니다.

