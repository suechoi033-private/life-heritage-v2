## 그리고, 당신 자신을 위해서도

이 글은 형식상 "부모님을 막 보낸 분"을 위한 것처럼 보이지만, 다시 한 번 들여다보면 **나 자신을 위한 안내**이기도 합니다.

내가 떠난 자리에서 가족이 검안의·경찰·보험사 앞에 앉아 똑같은 질문을 받게 됩니다. 내 평소 병명, 복용 약, 다니던 병원. 가족이 그걸 머리에서 끌어올리지 않아도 되게, 한 곳에 정리해 두는 일 — 그게 부모를 위해 시작한 기록을 **나의 사전 준비**로 잇는 자연스러운 다음 단계입니다.

이 글을 읽고 한 가지라도 덜 막막했다면, 같은 길을 곧 걷게 될 누군가에게 당신이 알게 된 것을 남겨 주세요. 그게 잇다가 만들어지는 방식입니다.

