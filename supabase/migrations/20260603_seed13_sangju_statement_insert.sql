## 흔히 놓치는 세 가지

**1. 사망진단서·시체검안서는 처음부터 7~10부 발급받기.**
사망신고·예금 인출·보험금 청구·연금·자동차/부동산 명의 이전 등 거의 모든 절차가 원본을 요구합니다. 한 번에 넉넉히 받아 두는 편이 다시 병원을 오가는 것보다 훨씬 낫습니다. 자세한 후속 절차는 [잇다 — 부모님을 갑자기 보낸 당신이 90일 안에 해야 할 일 12가지](./seed-01-bereavement-90days.md)에 정리해 두었습니다.

**2. 변사 사실확인원은 경찰서 방문 발급.**
보험금 청구에서 자주 요구되지만 정부24 온라인 발급이 안 됩니다. 관할 경찰서·지구대·파출소를 방문해 신청해야 하고, 사안에 따라 처리에 며칠~2주가 걸릴 수 있습니다.

**3. "왜 이렇게 캐묻나" 싶어도 정확하게 답하기.**
검안의·경찰의 질문은 의심이 아니라 절차입니다. 기억에 자신이 없는 부분은 추측으로 메우지 말고 "정확하지 않다, 확인 후 다시 말씀드리겠다"고 말해도 됩니다. 진술이 보정되면 시체검안서·조사기록도 보정될 수 있습니다.

