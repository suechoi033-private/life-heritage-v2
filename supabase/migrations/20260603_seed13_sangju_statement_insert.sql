## 미리 적어 둔 기록이, 그 자리에서 가장 큰 자산이 됩니다

이 글이 결국 하고 싶은 한 줄은 이것입니다.

> **상주진술서를 그 자리에서 잘 쓰는 비결은, 그 자리 이전에 이미 적혀 있는 기록을 갖고 있는 것입니다.**

부모님이 돌봄을 받기 시작한 1~2년 전부터 한 곳에 차곡차곡 쌓아 둔 면회 일지, 약 변경 이력, 처방전, 의사 소견 — 이런 것들이 사망 당일의 진술을 **떠올리는 것**이 아니라 **읽어 드리는 것**으로 만들어 줍니다. 검안의에게도, 경찰에게도, 보험사에게도 같은 일관된 사실을 전달할 수 있습니다. 무엇보다 형제끼리 "그때 어땠더라"로 다투지 않게 됩니다.

저희가 잇다를 만들면서 가장 자주 들은 후회가 이거였습니다. *"그때 누가 옆에서 '미리 한 곳에 기록해 두세요'라고만 알려줬어도."*

