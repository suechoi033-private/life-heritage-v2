## 세 자리에서 공통으로 묻는 다섯 가지

자리는 다르지만, 결국 묻는 것은 거의 같습니다.

1. **언제 어디서 돌아가셨는가** — 마지막으로 살아 계신 모습을 본 시각, 발견 시각, 장소.
2. **그날의 정황** — 직전 며칠의 증상(통증·호흡·의식·식사·낙상 등), 최근 병원 방문, 평소와 달랐던 점.
3. **평소 병력** — 진단받은 병명, 진단 시기, 다니던 병원, 담당 의사.
4. **복용 중이던 약** — 약 이름, 시작 시기, 최근 변경, 빠뜨린 적이 있었는지.
5. **사망 직전의 처치** — 응급실 이송 여부, 119 도착 전 처치, 가족이 한 행동.

문제는, 이걸 **정신이 가장 흐려진 그 시각에** 정확하게 떠올려야 한다는 점입니다. 약 이름은 안 외워지고, 진단 시기는 가물가물하고, "그때 그 병원이 어디였더라" 형제끼리 통화가 시작됩니다. 진술이 사실과 다르게 흘러가거나, 누가 무엇을 적었는지 가족 간에 기억이 엇갈리면 — 나중에 보험금 심사·상속·세무 절차에서 보정하기 어렵습니다.

