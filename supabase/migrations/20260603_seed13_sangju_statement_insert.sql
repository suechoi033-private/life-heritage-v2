## 잇다가 하는 일 — '그 자리'를 덜 막막하게

부모님을 돌보는 중이라면, 지금 이 순간이 그 진술을 위한 준비 기간이기도 합니다. 잇다 케어링은 그 기록을 흩어지지 않게 모아 둡니다.

- **케어링 일지** — 매주의 면회·증상 변화·의식·처치·의사 소견을 날짜별로 한 줄씩 남깁니다. 변화의 흐름이 한눈에 보입니다. (관련 글: [장기요양등급 신청부터 등급까지](./seed-04-long-term-care-application.md), [치료 중인 부모님, 형제와 함께 돌보기](./seed-03-1-care-together-siblings.md))
- **관리 항목(병명·약 보유)** — 진단받은 병명과 복용 중인 약을 **기간 보유**(시작·종료·만성 '계속')로 지속 관리합니다. "지금 무슨 약을 드시는지"를 그 자리에서 보여 드릴 수 있습니다.
- **처방전 사진 분석** — 처방전을 사진으로 찍어 두면 약물 카드(효능효과·총투약일수)로 자동 정리됩니다. 약 이름을 외우지 않아도 됩니다.
- **형제 공유 루프** — 카톡에 흩어지지 않고 한 자리에 쌓입니다. 누구든 같은 화면을 봅니다.

이 기록은 사망 당일의 진술뿐 아니라, 이후의 보험금 청구 — 보험사가 요구하는 진료 경과 자료 정리에도 그대로 쓰입니다.

