## 3. 네이버·카카오 — '열람'은 안 되고 '정리'는 됩니다

국내 플랫폼은 좀 다릅니다. 네이버·카카오 등은 개인정보 보호 원칙에 따라, 유족이라 해도 **고인의 이메일·대화 내용·사진을 열람하게 해 주지는 않습니다.** 계정 아이디·비밀번호는 본인에게만 속한 정보로 보아, 가족에게 넘겨주지 않는 것이 원칙입니다.

대신 **사망 사실을 확인해 계정을 정리(해지·삭제)**하는 것은 신청할 수 있습니다. 보통 사망진단서, 가족관계증명서, 신청인 신분증 사본 등을 갖춰 요청하면 됩니다. 카카오의 경우 직계가족 요청에 따른 '추모 프로필' 같은 별도 처리가 있을 수 있습니다.

*정리하면: 국내 플랫폼은 "안을 들여다보는 것"은 막혀 있고 "정리하는 것"은 가능합니다. 그러니 안에 있는 내용(중요한 메일, 사진)은 살아 있을 때 따로 백업해 두는 편이 안전합니다.*

각 플랫폼의 최신 정책·접수 방법은 네이버·카카오 고객센터에서 직접 확인하세요. (정책은 수시로 바뀝니다.)

