## 즉시 ~ 7일: 가장 먼저, 신고와 정지

**1. 사망신고 (사망 안 날부터 1개월 이내)**
시·읍·면·동 행정복지센터에 신고합니다. 병원에서 받은 사망진단서(또는 검안서)가 필요합니다. 정당한 사유 없이 기한을 넘기면 5만 원 이하의 과태료가 붙습니다. *팁: 사망진단서는 이후 은행·보험·연금에서 계속 요구하니, 처음부터 7~10부 넉넉히 발급받아 두세요.*
→ 근거: [정부24 '사망신고'](https://www.gov.kr/mw/AA020InfoCappView.do?CappBizCD=12700000059), [찾기쉬운 생활법령정보 '사망신고'](https://easylaw.go.kr/CSP/CnpClsMain.laf?popMenu=ov&csmSeq=707&ccfNo=6&cciNo=1&cnpClsNo=1)

**2. 고인 휴대폰·통신 정리**
명의자 사망 시 통신사에서 해지 또는 명의 승계를 처리합니다. 본인인증 문자가 와야 풀리는 계정이 많으니, 급하게 해지하기 전에 휴대폰으로 받아야 할 인증이 남았는지 먼저 확인하세요.

**3. 카드 자동이체·정기결제 확인**
고인 명의 카드로 빠져나가던 공과금·구독료를 파악해 둡니다. 계좌가 정지되면 자동이체가 막혀 연체로 이어질 수 있습니다. *팁: 통장 거래내역 최근 3개월치를 출력하면 빠진 것 없이 보입니다.*

