## 원칙 1. '공평'이 아니라 '명확'하게 나눈다

형제가 똑같이 부담하는 건 사실 불가능합니다. 사는 거리도, 소득도, 시간도, 부모와의 관계도 다 다릅니다. 그런데 "공평하게 하자"는 말에 매달리면, 모든 결정이 "이게 공평한가?"를 따지는 협상이 되고 결국 아무것도 정해지지 않습니다.

**대신 역할을 이름 붙여 나누세요.** 누가 무엇을 맡는지가 분명하면, 양이 조금 달라도 갈등은 훨씬 줄어듭니다.

- **주돌봄(메인 케어):** 일상 돌봄·간병인 소통·응급 상황 1차 대응
- **금전 담당:** 간병비·생활비 관리, 영수증 정리, 통장 관리
- **의료 동행:** 병원 예약·진료 동행·의사 면담 정리
- **행정 담당:** 장기요양등급 신청, 시설·서비스 알아보기, 서류 처리

한 사람이 두 역할을 겸할 수도 있고, 멀리 사는 형제가 금전·행정을 맡을 수도 있습니다. 중요한 건 "이건 내 몫"이라고 말할 수 있는 칸이 모두에게 있는 것입니다.

