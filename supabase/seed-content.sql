## 등급은 끝이 아니라 시작입니다

등급을 받고 나면, 그때부터 진짜 결정이 시작됩니다. 집에서 모실지 시설로 모실지, 어느 센터를 고를지, 형제끼리 어떻게 나눌지. 등급 신청은 그 모든 결정의 문을 여는 첫 행정 단계일 뿐입니다.

그리고 이 과정에서 한 가지를 미리 챙겨 두면 나중이 훨씬 수월합니다. **신청일, 방문조사 날짜, 의사소견서를 받은 병원, 통보받은 등급** — 이런 것들을 한 곳에 적어 두는 일입니다. 돌봄은 길고, 형제와 함께 가는 경우가 많으니, "그때 등급이 며칠에 나왔더라", "어느 병원에서 소견서를 받았더라"를 매번 다시 찾지 않으려면 처음부터 기록을 남겨 두는 편이 좋습니다.

그리고 이 글을 읽은 당신이 한 단계라도 덜 헤맸다면 — 같은 길을 걷는 다음 사람에게 당신이 알게 된 것을 남겨 주세요. 그게 잇다가 만들어지는 방식입니다.

