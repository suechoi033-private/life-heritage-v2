## 30일 ~ 60일: 재산을 '한 번에' 파악하기

**4. 안심상속 원스톱 서비스 신청 (사망일이 속한 달의 말일부터 1년 이내)**
이게 핵심입니다. [정부24](https://www.gov.kr/portal/onestopSvc/safeInheritance) 또는 행정복지센터에서 **한 번의 신청으로** 고인의 금융거래, 토지, 자동차, 국세·지방세, 국민연금 등 재산·채무 정보를 통합 조회할 수 있습니다. 흩어진 통장을 일일이 찾아다니지 않아도 됩니다.
*팁: 상속을 받을지 포기할지 판단하려면 빚이 얼마인지부터 알아야 합니다. 이 조회를 가장 먼저 하세요.*
→ 근거: [정부24 '안심상속 원스톱 서비스(사망자 재산조회 통합처리)'](https://www.gov.kr/portal/onestopSvc/safeInheritance)

**5. 고인의 빚·보증 확인**
4번 조회 결과에서 대출·세금 체납이 보입니다. 빚이 재산보다 많을 가능성이 있다면, 아래 6번(상속포기·한정승인)을 서둘러야 합니다. 기한이 짧기 때문입니다.

**6. 상속포기 또는 한정승인 결정 (상속개시를 안 날부터 3개월 이내)**
빚이 재산보다 많으면 **상속포기**(전부 안 받음) 또는 **한정승인**(받은 재산 한도 내에서만 빚 부담)을 가정법원에 신고할 수 있습니다. 이 3개월이 가장 놓치기 쉽고, 놓치면 빚까지 전부 떠안게 되는 가장 위험한 기한입니다.
*팁: 빚 규모가 애매하면 한정승인이 안전한 선택일 수 있습니다. 다만 절차가 까다로워 법무사 상담을 권합니다.*
→ 근거: [민법 제1019조](https://casenote.kr/%EB%B2%95%EB%A0%B9/%EB%AF%BC%EB%B2%95/%EC%A0%9C1019%EC%A1%B0), [찾기쉬운 생활법령정보 '상속의 승인·포기'](https://easylaw.go.kr/CSP/CnpClsMain.laf?popMenu=ov&csmSeq=255&ccfNo=4&cciNo=3&cnpClsNo=1)

**7. 국민연금 등 유족연금 청구**
고인이 국민연금 가입자였다면 유족연금을 받을 수 있습니다. 국민연금공단 지사 방문뿐 아니라 정부24·[국민연금공단 홈페이지(전자민원)](https://www.nps.or.kr/elctcvlcpt/comm/getOHAC0000M5.do?menuId=MN24001074)나 우편으로도 청구할 수 있습니다. 소멸시효는 5년이지만, 늦을수록 절차가 복잡해지니 이 시기에 함께 처리하는 게 좋습니다. (대표전화 1355)
*팁: 공무원·사학·군인연금은 각 공단으로 따로 문의해야 합니다.*
→ 근거: [국민연금공단 '유족연금'](https://www.nps.or.kr/pnsinfo/ntpsklg/getOHAF0072M0.do)

**8. 보험금 청구**
생명보험·실손보험·상해보험 등에 가입돼 있었는지 확인해 청구합니다. 안심상속 조회에는 보험 가입 내역도 일부 포함되며, [내보험찾아줌](https://cont.insure.or.kr/)(생명·손해보험협회) 서비스로도 숨은 보험을 찾을 수 있습니다.

