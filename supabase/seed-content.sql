## 원칙 3. 돈 문제는 '처음에' 규칙을 정한다

돈은 나중에 말하기 가장 어려운 주제입니다. 그래서 돌봄이 한참 진행된 뒤 한꺼번에 터집니다. "간병비를 나만 냈다", "엄마 통장에서 얼마가 나갔는지 모르겠다" 같은 말은 관계를 가장 빠르게 망가뜨립니다.

부담이 적을 때, 즉 **시작할 때** 규칙을 정해 두세요.

- 간병비·생활비를 어떻게 나눌지(균등 / 소득 비례 / 가까운 사람은 시간으로, 먼 사람은 돈으로)
- 부모님 자산(통장·연금)에서 나가는 돈과 형제가 보태는 돈을 구분
- **영수증·지출 내역을 모두가 볼 수 있게 공유** — 금액보다 '투명함'이 신뢰를 지킵니다

돈 이야기를 미리 꺼내는 건 야박한 게 아닙니다. 나중에 서로를 의심하지 않기 위한 배려입니다. 특히 부모님 통장에서 나가는 돈은 나중에 형제 사이 오해로 번지기 가장 쉬우니, 누가 관리하든 내역을 다 같이 보는 구조를 처음부터 만들어 두세요.

## 원칙 4. 멀리 사는 형제의 몫을 '따로' 정한다

해외나 지방에 사는 형제는 "나는 멀어서 못 한다"고 빠지기 쉽고, 가까운 형제는 그걸 보며 서운해합니다. 하지만 거리가 멀어도 할 수 있는 일은 분명히 있습니다.

- **금전 기여:** 직접 못 가는 대신 간병비를 더 부담
- **행정·정보 조사:** 시설 비교, 제도 알아보기, 서류 작성 등 비대면으로 가능한 일
- **정기 교대:** 한 달에 한 번, 명절·연휴에 내려와 주돌봄자에게 '쉬는 날'을 주기
- **소통 담당:** 형제 그룹에 상황을 정리해 공유하는 역할

핵심은 "멀어서 못 한다"를 "멀어서 이걸 맡는다"로 바꾸는 것입니다. 빈칸을 남겨 두지 마세요.

