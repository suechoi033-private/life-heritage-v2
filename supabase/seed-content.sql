## 원칙 2. 의료 정보와 결정은 '한 곳'에 기록한다

가장 많은 오해가 여기서 생깁니다. 의사가 한 말, 약 변경, 다음 검사 일정을 주돌봄자가 머릿속에 담아 두었다가 단톡방에 그때그때 흘리면, 다른 형제는 조각난 정보만 받습니다. 그러다 "나는 못 들었다"가 나오고, 서운함이 쌓입니다.

어려운 의료 정보를 카톡으로 일일이 옮겨 적는 일 자체가 큰 부담입니다. 메시지는 위로 흘러가 사라지고, 정작 필요할 때 찾을 수가 없습니다.

**해법은 단순합니다. 흘러가는 대화방 말고, 쌓이는 기록 한 곳을 정하세요.** 진료 날짜, 의사 소견, 약 변경, 다음에 결정할 일을 한 자리에 적어 두면, 멀리 있는 형제도 같은 페이지를 봅니다. "언제 누가 무엇을 들었나"가 남으니, 나중에 기억이 엇갈려 다툴 일도 줄어듭니다.

