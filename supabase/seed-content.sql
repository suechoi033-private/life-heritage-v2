## 90일 안: 명의 이전과 세금 준비

**9. 예금·금융자산 상속 처리**
상속인이 확정되면 은행에서 예금을 인출·해지하거나 명의를 정리합니다. 보통 상속인 전원의 동의 서류(가족관계증명서·상속인 전원 인감 등)가 필요합니다. *팁: 은행마다 요구 서류가 다르니, 방문 전 전화로 목록을 받아 한 번에 챙겨가세요.*

**10. 자동차 상속 이전등록 (사망일이 속한 달의 말일부터 6개월 이내)**
고인 명의 차량이 있다면 차량등록사업소에서 이전등록을 합니다. 기한을 넘기면 과태료가 부과됩니다. 차를 처분할 계획이라도 일단 상속 이전 후 매매해야 합니다.
→ 근거: [시·도 차량등록사업소 '소유권 이전등록(상속)'](https://www.busan.go.kr/car/crdocument/9636)

**11. 부동산 상속등기 — 등기 자체는 기한이 없지만, 취득세는 6개월**
부동산을 상속인 명의로 옮기는 등기에는 법정 기한이 없습니다. 다만 **취득세는 사망일이 속한 달의 말일부터 6개월 이내**에 신고·납부해야 하며, 넘기면 가산세가 붙습니다. 등기를 미루더라도 세금 기한은 챙기세요.
→ 근거: [찾기쉬운 생활법령정보 '상속에 의한 소유권 이전등기'](https://easylaw.go.kr/CSP/CnpClsMain.laf?popMenu=ov&csmSeq=566&ccfNo=3&cciNo=3&cnpClsNo=1)

**12. 상속세 신고·납부 (사망일이 속한 달의 말일부터 6개월 이내)**
상속재산이 일정 규모를 넘으면 관할 세무서에 상속세를 신고합니다. 배우자·자녀 공제 등으로 실제로는 세금이 없는 경우도 많지만, **신고 자체는 해 두는 게 안전**합니다(추후 양도세 계산의 기준이 됩니다). 재산 규모가 크거나 복잡하면 세무사 상담을 권합니다.
→ 근거: [국세청 '상속세 신고시 유의사항'](https://www.nts.go.kr/nts/cm/cntnts/cntntsView.do?mi=2328&cntntsId=7722)

